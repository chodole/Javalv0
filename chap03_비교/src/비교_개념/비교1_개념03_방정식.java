package 비교_개념;

public class 비교1_개념03_방정식 {
	public static void main(String[] args) {

		/*
			[문제]
				a - 1 == -9; 
				위식의 답이 true 가 나오도록 a의 값을 구하시오. 

			[정답]
				-8
		*/
		
		
		int a = 0;
		a = -9 + 1;
		boolean b = a - 1 == -9;
		System.out.println(a);
		System.out.println(b);
	}

}
