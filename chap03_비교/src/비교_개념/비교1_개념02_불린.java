package 비교_개념;

public class 비교1_개념02_불린 {

	public static void main(String[] args) {
		
		/*
		  	[boolean 자료형]
		 		true 와 false를 저장할수있다.
		 		즉, 비교연산자의 결과를 저장할수있다.
		 */
		
		boolean a = true;
		boolean b = false;
		
		System.out.println(a);
		System.out.println(b);
		/*
			비교연산자의 결과를 저장할수있다.
		*/
		boolean c = 10 == 10;
		boolean d = 10 != 10;
		
		System.out.println(c);
		System.out.println(d);
		

	}

}
