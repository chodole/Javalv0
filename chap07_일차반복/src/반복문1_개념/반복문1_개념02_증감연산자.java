package 반복문1_개념;

public class 반복문1_개념02_증감연산자 {
	public static void main(String[] args) {

		 /*
	        [증감연산자]
	            [1] ++ 을 변수 앞이나 뒤에 붙이면 값 1이 증가된다.  
	            [2] -- 을 변수 앞이나 뒤에 붙이면 값 1이 감소된다.       
		*/
	
	    int a = 10;
	    a ++;
	    System.out.println(a);
	
	    int b = 10;
	    ++b;
	    System.out.println(b);
	
	    int c = 10;
	    c--;
	    System.out.println(c);
	
	    int d = 10;
	    --d;
	    System.out.println(d);	
	}
}
