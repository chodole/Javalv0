package 반복문1_개념;

public class 반복문1_개념01_산술연산자축약형 {
	public static void main(String[] args) {
		/*
        [산술연산자 축약형]
            산술연산자 + - * / % 의 축약형이다. 
		 */
	    int a = 10;
	    a += 3;
	
	    int b = 10;
	    b -= 3;
	
	    int c = 10;
	    c *= 3;
	
	    int d = 10;
	    d /= 3;
	
	    int e = 10;
	
	    e %= 3;
	
	    System.out.println(a + " " + b + " " + c + " " + d + " " + e);
	
	    int a1 = 10;
	    a1 = a1 + 3;
	
	    int b1 = 10;
	    b1 = b1 - 3;
	
	    int c1 = 10;
	    c1 = c1 * 3;
	
	    int d1 = 10;
	    d1 = d1 / 3;
	
	    int e1 = 10;
	    e1 = e1 % 3;
	
	    System.out.println(a1 + " " + b1 + " " + c1 + " " + d1 + " " + e1);
	}
}
