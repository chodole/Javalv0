package 문자열_개념;

public class 문자열1_개념01_char {
	public static void main(String[] args) {
		/*
		  	문자 한글자는 character 라고하며 길이가 길기때문에 char 를 사용한다. 
		  	char 는 한글자만 저장가능하며 홑따옴표 '' 를 감싼다. 
		  	  	
		 */
		
		char a = 'a';
		System.out.println(a);
		
		char b = '김';
		System.out.println(b);
		
	}
}
