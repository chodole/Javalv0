package 문자열_개념;

public class 문자열1_개념03_String {
	public static void main(String[] args) {	
		/*
		  	문자에 배열을 사용하면 엄청 불편함이 따르기때문에 String 을 제공한다. 
		  	문자열은 원래는 배열이지만 마치 변수하나인것처럼 사용가능하다.
		 	데이터저장은 쌍따옴표를 "" 감싼다.
		 */	
		String str = "abcde";
		System.out.println(str);
		
		String name = "김철수";
		System.out.println(name);
	}
}
