package 문자열_개념;

public class 문자열1_개념06_길이 {
	public static void main(String[] args) {
		
		
		int[] arr = {10, 20, 30};
		System.out.println("배열의 길이 = " + arr.length); // 배열은 () 가 붙지않는다.
		
		String str = "itacademy";
		System.out.println("문자열의 길이 = " + str.length()); // 문자열은 () 가 붙는다. 주의!!
		
		int size = str.length();
		System.out.println(size);
		
	
		
	}
}
