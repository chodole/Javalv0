package 배열_개념;

public class 배열1_개념04_길이 {
	public static void main(String[] args) {
		int[] arr = {1, 2, 3, 4};
		
		for(int i = 0; i < 4; i++) {
			System.out.println(arr[i]);
		}

		System.out.println("배열의 개수 = " + arr.length);
		
		for(int i = 0; i < arr.length; i++) {
			System.out.println(arr[i]);
		}
		

	}
}
