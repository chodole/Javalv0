package 기본_개념;

public class 기본1_개념04_출력문종류 {
	public static void main(String[] args) {
		
		/*
			출력문의 종류
			System.out.
			(1) 줄 바꿈이 있는 출력 : System.out.println("문자")
			(2) 줄 바꿈이 없는 출력 : System.out.print("문자")
			(3) 줄 바꿈 : System.out.println()
		*/
		System.out.print("안");
		System.out.print("녕");
		
		// 아무 내용도 안 적으면 줄 바꿈이 된다.
		// ln :  new line
		System.out.println();
		
		System.out.print("하세요");
		
		
	}
}
