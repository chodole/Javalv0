package 기본_개념;

public class 기본2_개념01_산술연산자 {
	public static void main(String[] args) {
		
		/*
			[1] 산술 연산자
				1. 더하기			+
				2. 빼기			-
				3. 곱하기			*
				4. 나누기			/
				5. 나머지			%
			
		 */
		System.out.println(10 + 3);
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		
		// 정수 / 정수 = 정수(몫)
		System.out.println(10 / 3);
		// 정수 / 실수 = 실수
		// 실수 / 정수 = 실수
		// 실수 / 실수 = 실수
		System.out.println(10.0 / 3);
		System.out.println(10 / 3.0);
		System.out.println(10.0 / 3.0);
		
		System.out.println(10 % 3);
		
		
	}
}
